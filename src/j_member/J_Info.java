package j_member;

public class J_Info {
	private int i_no;
	private String i_email;
	private String i_passwd;
	private String i_nick;
	private String c_value;
	private String l_value;

	public int getI_no() {
		return i_no;
	}

	public void setI_no(int i_no) {
		this.i_no = i_no;
	}

	public String getI_email() {
		return i_email;
	}

	public void setI_email(String i_email) {
		this.i_email = i_email;
	}

	public String getI_passwd() {
		return i_passwd;
	}

	public void setI_passwd(String i_passwd) {
		this.i_passwd = i_passwd;
	}

	public String getI_nick() {
		return i_nick;
	}

	public void setI_nick(String i_nick) {
		this.i_nick = i_nick;
	}

	public String getC_value() {
		return c_value;
	}

	public void setC_value(String c_value) {
		this.c_value = c_value;
	}

	public String getL_value() {
		return l_value;
	}

	public void setL_value(String l_value) {
		this.l_value = l_value;
	}

}
